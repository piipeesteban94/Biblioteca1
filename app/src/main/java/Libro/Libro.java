package Libro;

public class Libro {

    private int Farenheit;
    private int Revival;
    private int ElAlquimista;

    public Libro() {
        Farenheit = 5000;
        Revival = 12000;
        ElAlquimista = 45000;
    }

    public int getFarenheit() {
        return Farenheit;
    }

    public int getRevival() {
        return Revival;
    }

    public int getAlquimista() {
        return ElAlquimista;
    }
}
